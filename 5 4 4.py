import numpy as np
from scipy.stats import chi2_contingency

# Данные о покупках товаров X и Y в двух торговых центрах
observed = np.array([[28, 12], [21, 19]])

# Вычисляем критерий хи-квадрат и p-значение
chi2, p_value, _, _ = chi2_contingency(observed)

# Выводим результаты
print("Критерий хи-квадрат:", chi2)
print("p-значение:", p_value)

# Проверяем наличие статистической значимости на уровне значимости 5%
alpha = 0.05
if p_value < alpha:
    print("Рекламу можно считать эффективной на уровне значимости 5%.")
else:
    print("Рекламу нельзя считать эффективной на уровне значимости 5%.")

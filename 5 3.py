import numpy as np
from scipy.stats import t

# Данные роста и массы студентов
height = np.array([171, 181, 175, 182, 189, 183, 182, 176, 190, 181, 179, 173, 171, 170, 160, 187, 175])
weight = np.array([48, 80, 72, 67, 82, 73, 72, 127, 100, 70, 72, 73, 67, 60, 60, 69, 79])

# Вычисляем выборочный коэффициент корреляции
correlation = np.corrcoef(height, weight)[0, 1]

# Вычисляем стандартную ошибку коэффициента корреляции
n = len(height)
standard_error = np.sqrt((1 - correlation**2) / (n - 2))

# Уровень значимости
alpha = 0.05

# Рассчитываем t-статистику
t_statistic = correlation / standard_error

# Рассчитываем количество степеней свободы
df = n - 1

# Рассчитываем p-значение
p_value = 2 * (1 - t.cdf(abs(t_statistic), df))

# Выводим результаты
print("Выборочный коэффициент корреляции:", correlation)
print("Стандартная ошибка коэффициента корреляции:", standard_error)
print("t-статистика:", t_statistic)
print("p-значение:", p_value)

if p_value < alpha:
    print("Отвергаем нулевую гипотезу: есть статистически значимая корреляция между ростом и массой.")
else:
    print("Не отвергаем нулевую гипотезу: нет статистически значимой корреляции между ростом и массой.")
